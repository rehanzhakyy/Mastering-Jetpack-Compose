fun main() {
    // Arithmetic Operators
    val a = 8
    val b = 2
    // Arithmetic Operators Output
    println(a + b)
    println(a - b)
    println(a * b)
    println(a / b)
    println(a % b)

    // Assignment Operators
    var c = 10
    c += 6
    c -= 6
    c /= 6
    c %= 6

    // Assignment Operators Output
    println(c)

    val k: Float = 7f
    val l: Float = 3f

    println(k / l)
}