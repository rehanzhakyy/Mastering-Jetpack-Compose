fun main() {
    // Arithmetic Operators
    var a = 8
    var b = 2
    // Arithmetic Operators Output
    println(a + b)
    println(a - b)
    println(a * b)
    println(a / b)
    println(a % b)

    // Assignment Operators
    var c = 20
    c += 6
    c -= 6
    c /= 6
    c %= 6

    // Assignment Operators Output
    println(c)

    val k: Float = 7f
    val l: Float = 3f

    println(k / l)

    // Increment
    println(a++)
    println(a)

    // Decrement
    println(a--)
    println(a)

    // Pre Increment
    println(++a)

    // Post Increment
    println(a++)
    println(a)

    // Pre Decrement
    println(--a)

    // Post Decrement
    println(a--)
    println(a)


}