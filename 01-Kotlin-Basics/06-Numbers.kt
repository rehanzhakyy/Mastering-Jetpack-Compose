fun main() {

    // Bytes
    val a: Byte = -128
    val b: Byte = 127

    // Short
    val c: Short = -32768
    val d: Short = 32767

    // Integer
    val e: Int =  -2147483648
    val f: Int = 2147483647

    // Mencetak Bytes
    println(a)
    println(b)

    // Mencetak Short
    println(c)
    println(d)

    // Integer
    println(e)
    println(f)
}