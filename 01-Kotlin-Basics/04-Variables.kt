fun main() {

    // var variableName = value
    // val variableName = value

    var name = "Raihan"  // String (Text)
    val age = 23         // Int (Number)

    // String Concatenation
    println("Hello " + name)

    // String Template
    println("My age $age")

    // Var can be reassigned
    name = "Zhaky"

    // Val cannot be reassigned
    // age = 27, remove (//) to see the error
}